#include "../cub3D.h"

void	clean_tab(char ***arg)
{
	int	i;

	i = 0;
	if (*arg && **arg)
	{
		while ((*arg)[i])
		{
			free((*arg)[i]);
			i++;
		}
		free(*arg);
		*arg = NULL;
	}
}
